// argument vs parameter

function example(parameter) {
    console.log(parameter);
}

const argument = 'foo';

example(argument);

/**
 * Parameter (FormalParameter)
 * 형식을 갖춘, 매개변수
 */

function axios(url){
    //some code
}

/**
 * Argument(Actual Parameter)
 * 
 * 실제로 사용되는 , 인자 or 인수
 */

axios('http://github.com')